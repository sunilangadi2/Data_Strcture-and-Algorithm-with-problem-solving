""" Built in swapcase() function converts lowercase letter to uppercase letters
    and vice-versa """

string="su*iLS"
print(string)
print("After swap",string.swapcase())
print(string.upper())
print(string.lower())

# Just to capitalize first letter
print(string.capitalize())